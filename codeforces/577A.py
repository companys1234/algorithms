import math

n, x = map(int, input().split())

c = 0
# Ограничиваем цикл корнем из x, приведенным к целому числу
limit = int(math.sqrt(x))

for i in range(1, limit + 1):
    if x % i == 0:
        j = x // i
        # Проверяем, вписываются ли делители i и j в таблицу n x n
        if i <= n and j <= n:
            if i == j:
                c += 1  # Если это квадрат (например, 4*4=16), считаем один раз
            else:
                c += 2  # Если разные (например, 2*8=16 и 8*2=16), считаем дважды
print(c)