t = int(input())  # Количество тестов

for _ in range(t):
    a, b, c = map(int, input().split())  # Читаем три числа

    possible = False  # Флаг: можно ли сделать АП

    # ============================================
    # СЛУЧАЙ 1: Умножаем первое число a на m
    # Новая прогрессия: [a*m, b, c]
    # Условие АП: 2*b = a*m + c
    # Отсюда: m = (2*b - c) / a
    # ============================================
    numerator = 2 * b - c  # Числитель для m
    if numerator > 0 and numerator % a == 0:  # m должно быть >= 1 и целым
        possible = True

    # ============================================
    # СЛУЧАЙ 2: Умножаем второе число b на m
    # Новая прогрессия: [a, b*m, c]
    # Условие АП: 2*(b*m) = a + c
    # Отсюда: m = (a + c) / (2*b)
    # ============================================
    numerator = a + c  # Числитель для m
    denominator = 2 * b  # Знаменатель для m
    if numerator % denominator == 0 and numerator // denominator >= 1:
        possible = True

    # ============================================
    # СЛУЧАЙ 3: Умножаем третье число c на m
    # Новая прогрессия: [a, b, c*m]
    # Условие АП: 2*b = a + c*m
    # Отсюда: m = (2*b - a) / c
    # ============================================
    numerator = 2 * b - a  # Числитель для m
    if numerator > 0 and numerator % c == 0:  # m должно быть >= 1 и целым
        possible = True

    # ============================================
    # ВЫВОД РЕЗУЛЬТАТА
    # ============================================
    if possible:
        print('YES')
    else:
        print('NO')